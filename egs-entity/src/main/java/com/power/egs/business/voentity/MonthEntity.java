package com.power.egs.business.voentity;

import java.io.Serializable;
import java.util.ArrayList;
import java.util.List;
import java.util.Map;

/**
 * 本月统计定义
 * @author haitao
 *
 */
public class MonthEntity implements  Serializable{
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	String dateofmonth;   //三月4号
	String datetime;    //日期
	String totalvolume;  //总升数
	List<Map<String,String>> oillist=new ArrayList<Map<String,String>>(); //map的key是油品名称，value是升数
	public String getDateofmonth() {
		return dateofmonth;
	}
	public void setDateofmonth(String dateofmonth) {
		this.dateofmonth = dateofmonth;
	}
	public String getDatetime() {
		return datetime;
	}
	public void setDatetime(String datetime) {
		this.datetime = datetime;
	}
	public String getTotalvolume() {
		return totalvolume;
	}
	public void setTotalvolume(String totalvolume) {
		this.totalvolume = totalvolume;
	}
	public List<Map<String, String>> getOillist() {
		return oillist;
	}
	public void setOillist(List<Map<String, String>> oillist) {
		this.oillist = oillist;
	}
	
}

